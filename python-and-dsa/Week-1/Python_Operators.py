# Arithmetic Operators in Python
# Variables
a = 15
b = 4
# Addition
print("Addition:", a + b)
# Subtraction
print("Subtraction:", a - b)
# Multiplication
print("Multiplication:", a * b)
# Division
print("Division:", a / b)
# Floor Division
print("Floor Division:", a // b)
# Modulus
print("Modulus:", a % b)
# Exponentiation
print("Exponentiation:", a ** b)
# Relational Operators
a = 13
b = 33
print(a > b)
print(a < b)
print(a == b)
print(a != b)
print(a >= b)
print(a <= b)
# Logical Operators
a = True
b = False
print(a and b)
print(a or b)
print(not a)
# Bitswise Operators
a = 10
b = 4
print(a & b) # AND
print(a | b) # OR
print(~ a) # NOT
print(a ^ b) # XOR
print(a << 2) # Left Shift
print(a >> 2) # Right Shift
# Assignment Operators
a = 10
b = a
print(b)
b += a
print(b)
b -= a
print(b)
b *= a
print(b)
b <<= a
print(b)